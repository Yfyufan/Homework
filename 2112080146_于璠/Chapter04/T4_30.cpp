#include <iostream>

using namespace std;

int main()
{
    double pi=3.14159,d,c,s,r;
    cin>>r;
    d=2*r;
    c=2*r*pi;
    s=pi*r*r;
    cout<<"直径："<<d<<endl;
    cout<<"周长："<<c<<endl;
    cout<<"面积："<<s<<endl;
    return 0;
}
